function changeLang(lang) {
    // Sauvegarder la langue choisie pour la persistance entre les pages
    localStorage.setItem('portfolio_lang', lang);
    document.querySelectorAll('.lang-btn').forEach(btn => btn.classList.remove('active'));
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.getAttribute('onclick') && btn.getAttribute('onclick').includes("'" + lang + "'")) {
            btn.classList.add('active');
        }
    });

    const currentLangBtn = document.getElementById('current-lang');
    if (currentLangBtn) {
        if (lang === 'fr') currentLangBtn.innerHTML = '<img src="https://flagcdn.com/w20/fr.png" alt="FR" width="20" style="margin-right:5px;"> FR ▼';
        if (lang === 'en') currentLangBtn.innerHTML = '<img src="https://flagcdn.com/w20/gb.png" alt="EN" width="20" style="margin-right:5px;"> EN ▼';
        if (lang === 'de') currentLangBtn.innerHTML = '<img src="https://flagcdn.com/w20/de.png" alt="DE" width="20" style="margin-right:5px;"> DE ▼';
    }

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });
    document.documentElement.lang = lang;
}

// Restaurer automatiquement la langue sauvegardée à chaque chargement de page
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('portfolio_lang') || 'fr';
    changeLang(savedLang);
});
